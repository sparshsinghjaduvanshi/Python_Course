'''The vasiac idea of oop in python is to use 
    classes and objects to represent real-world 
    concepts and entities.'''

'''A class is a blueprint or template for creating objects/it defines the properties and 
    methods that an objects of that class will have. Properties are the data or 
    state of an object, and methods are the actions or behaviour that an object can
    perform.'''
'''An object is an instance of a class, and it contains its own data and methods. For 
    example,you could create a class called "person" that has prperties such as name and 
    age and methods such as speak() and walk(). Each instance of the person class 
    would be a uoque object with its own name and age, but they would all have the 
    same methods to speak and walk.'''

'''One of the key features of OOP in Python is encapsulation , which means that the 
    internal state of an object is hidden and can only be accessed or modified through 
    the object's methods .This helps to protect the object's data and prevent it from
    being modified in unexpected ways.'''
'''Another key feature of the OOP in Python is inheritance ,ehich allows new classes to be 
    created that inherit the properties and methods of an existing class .This allows for code
    to reuse and makes it easy to create new classes that have similar functionality to existing 
    classes'''

'''Polymorphism is also supported in Python ,which means that objects of different classes
    can be treated as if they were objects of a common class. This allows for freater flexibility
    in code andd makes it easier to write code that can work with multiple types of objects.'''

'''In summary ,OOP in Python allows develops to model real-world concepts and entities 
    using classes and objects,encapsulate data,reuse code through inheretance, and write
    more flexible code through polymorphism.'''